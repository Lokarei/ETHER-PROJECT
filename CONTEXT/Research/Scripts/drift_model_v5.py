#!/usr/bin/env python3
"""
Дрейф ЭВО v5: самосогласованный КУБК (p_канал/p_СЭ = 1)
=========================================================
B (Математика), 2026-03-25
Директива: A-DIR-KUBK-001 + указания руководителя от 2026-03-25 (п.2.1–2.4)
Ссылки: CANON v22 §1, §2.3, §4.3–4.6, §5.2, §6, §8, §17; материалы «Кинетика1–4».

КИНЕТИЧЕСКОЕ ОБОСНОВАНИЕ ФОРМУЛ
--------------------------------
1. Давление (интенсивность потока импульса) СЭ на ГП:
   p_СЭ = m_a·v₀²/(4·d_A²) (CANON §2.3, Кинетика4)
   Вывод: частота ударов о единичную площадку ГП со стороны хаотической фазы
   = n_a·v_avg/4, где v_avg = v₀·2/√π для максвелловского распределения,
   но в модели принята детерминированная оценка, дающая p_СЭ = m_a·v₀²/(4·d_A²).

2. Давление (поток импульса) со стороны канала:
   В канале действует газовый режим (n_ch ≪ n_a). Амеры канала имеют
   направленную компоненту v_n (вдоль h_A) и хаотическую v (изотропную в системе
   потока). Согласно §4.6, v_n скользит вдоль ГП, а нормальная компонента скорости
   при столкновении определяется только v. Частота ударов о единичную площадку ГП
   изнутри = n_ch·v/4. Передаваемый импульс за удар = 2·m_a·v·(v·n̂)/v = 2·m_a·v·cosθ.
   Усреднение по полусфере даёт p_канал = n_ch·m_a·v²/(4·d_A²).

3. КУБК (CANON §4.5): баланс числа контактов (не потоков импульса) приводит к
   n_ch·v² = n_a·v₀², откуда p_канал = p_СЭ (самосогласование).

4. Дрейф:
   - Волна шнека деформирует ГП, создавая наклон нормали.
   - v_n, скользя вдоль ГП, даёт проекцию на деформированную нормаль:
     cos_tilt = (v_n·n̂)/|v_n| = O(A·k_z).
   - При cos_tilt < 0 Амер канала набегает на наклонный участок и передаёт
     импульс, создавая локальный дисбаланс потоков импульса.
   - Интеграл этого дисбаланса по Σ даёт вращающий момент Ω(χ), который через
     спиральную связь (параметр намотки q) преобразуется в дрейф V_∥(χ·s_sh).

Все величины «давление», «сила» в коде являются макро-ярлыками для статистических
потоков импульса (CANON §1). Переменные F_* обозначают интегралы разности
потоков импульса через Σ, а не причинные силы.
"""

import numpy as np

# ============================================================
# ФУНДАМЕНТАЛЬНЫЕ ПАРАМЕТРЫ (CANON §2.2–2.3)
# ============================================================
d_A = 1.0            # диаметр амера [мера длины]
v_0 = 1.0            # среднеквадратичная скорость амера в СЭ [мера скорости]
m_a = 1.0            # масса амера
n_a = 16/(27*np.sqrt(2)*d_A**3)   # объёмная концентрация амеров в СЭ (Гаусс)

# Интенсивность потока импульса от СЭ на ГП (макро-ярлык «давление»)
# Формула из CANON §2.3, Кинетика4
p_SE = m_a * v_0**2 / (4 * d_A**2)

# Геометрия квазицилиндрического патча (CANON §5.2)
R_major = 2500.0     # большой радиус тора [d_A]
r_minor = 50.0       # малый радиус канала [d_A]
delta_ch = 20.0      # толщина канала (не используется в текущей версии)

# Дискретизация ГП
N_TH = 512           # число узлов по θ (большая окружность)
N_PSI = 512          # число узлов по ψ (вдоль образующей)


# ============================================================
# ПАРАМЕТРЫ ВОЛНЫ ШНЕКА (CANON §6)
# ============================================================
def wave_params(r, m_S=1):
    """
    Параметры шнековой моды.
    m_S = 1,2,4 — базовые моды (S1,S2,S4).
    Возвращает волновые числа и минимальную длину патча.
    """
    k = 1.0 / (m_S * r)               # волновое число в θ-направлении
    kz = k / np.sqrt(2)               # проекция на ось z (в локальной системе)
    Lz_min = np.sqrt(2) * np.pi * m_S * r   # минимальная длина патча для полуволны
    return {'k_z': kz, 'k_mag': k, 'Lz_min': Lz_min}


def close_amplitude(r, m_S=1):
    """Амплитуда шнека из условия A·k_z = 0.1 (линейный режим)."""
    kz = wave_params(r, m_S)['k_z']
    return 0.1 / kz


# ============================================================
# КУБК-ЗАМЫКАНИЕ (CANON §4.5–4.6)
# ============================================================
def close_params(v_ratio=2.0):
    """
    Определяет параметры канала из КУБК-условия и порога устойчивости.
    КУБК: n_ch·v² = n_a·v₀² (баланс числа контактов)
    Порог: |v_n| ≥ 2|v| (устойчивость вихря, §4.3)
    Рабочее значение: |v_n| = 2|v| (нижний порог)
    """
    v_n = 3.0 * v_0                     # типовое значение направленной скорости
    v_ch = v_n / v_ratio                # хаотическая компонента в канале
    n_ch = v_0**2 / v_ch**2             # из n_ch·v_ch² = v₀² (здесь n_a сокращается)
    return {'v_n': v_n, 'v_ch': v_ch, 'n_ch': n_ch}


# ============================================================
# ДЕФОРМАЦИЯ ГРАНИЧНОЙ ПОВЕРХНОСТИ (волна шнека)
# ============================================================
def deformed_normal(TH, PSI, chi, s_sh, kz, A):
    """
    Вычисляет единичную нормаль к деформированной ГП.
    Смещение: δρ = -A·sin(kz·z + kθ·θ − ωt), где ω = s_sh·v_phase.
    Здесь TH = θ, PSI = φ (или z). Упрощённо: ωt = 0, так как волна бегущая.
    """
    c = A * s_sh * kz * np.cos(PSI)          # производная смещения по z
    n0x = np.cos(TH); n0y = np.sin(TH)       # нормаль к недеформированному цилиндру
    ex = -np.sin(TH); ey = np.cos(TH)        # касательные направления
    # Нормаль после деформации (линейное приближение)
    nx = n0x - chi * c * ex
    ny = n0y - chi * c * ey
    nz = -c * np.ones_like(TH)
    mag = np.sqrt(nx**2 + ny**2 + nz**2)
    return nx / mag, ny / mag, nz / mag


# ============================================================
# ПОТОКИ ИМПУЛЬСА СО СТОРОНЫ КАНАЛА (КУБК-самосогласованные)
# ============================================================
def channel_forces(TH, PSI, chi, s_sh, v_n, v_ch, n_ch, kz, A):
    """
    Интегралы разности потоков импульса через Σ со стороны канала.
    Все величины трактуются как статистические потоки импульса (макро-ярлыки).

    Разделение ролей v_n и v (CANON §4.6):
    - v_n (направленная) → ГЕОМЕТРИЯ смещения мест столкновений.
      На гладкой ГП v_n скользит (v_n·n̂₀=0). Деформация создаёт наклон,
      и проекция v_n на деформированную нормаль становится ненулевой.
    - v (хаотическая) → ИНТЕНСИВНОСТЬ обмена импульсом, фиксируемая КУБК.

    Возвращает:
      Fz   – интеграл потока импульса по направлению z (компонента),
      Fth  – интеграл потока импульса по касательному направлению θ,
      p_base – изотропный поток импульса (макро-ярлык «давление») от v.
    """
    nx, ny, nz = deformed_normal(TH, PSI, chi, s_sh, kz, A)
    ex = -np.sin(TH); ey = np.cos(TH)

    # --- Направленная компонента v_n (определяет геометрию) ---
    vn_x = v_n / np.sqrt(2) * chi * ex
    vn_y = v_n / np.sqrt(2) * chi * ey
    vn_z = v_n / np.sqrt(2) * np.ones_like(TH)

    # Проекция v_n на деформированную нормаль – угловой фактор смещения
    vn_dot_n = vn_x * nx + vn_y * ny + vn_z * nz
    cos_tilt = vn_dot_n / v_n          # = O(A·k_z) при малых A

    # --- Хаотическая компонента v (интенсивность обмена) ---
    # Изотропный поток импульса (макро-ярлык «давление»), создаваемый v
    p_base = n_ch * m_a * v_ch**2 / (4 * d_A**2)   # = p_SE при КУБК

    # Дисбаланс от наклона ГП: дополнительный поток импульса, возникающий
    # из-за того, что Амеры с направленной скоростью v_n набегают на наклон.
    # Интенсивность пропорциональна n_ch·m_a·v_ch²·cos_tilt², знак определяется
    # направлением набегания (cos_tilt < 0).
    drift_coeff = n_ch * m_a * v_ch**2 * cos_tilt**2
    mask = (cos_tilt < 0).astype(float)
    drift_coeff *= mask

    # Базовый (изотропный) вклад: поток импульса от v (всегда внутрь, т.к. n̂ наружу)
    Fz_base = p_base * (-nz)
    Fth_base = p_base * (-(-np.sin(TH) * nx + np.cos(TH) * ny))

    # Дополнительный вклад от наклона (дрейфовая компонента)
    Fz_drift = drift_coeff * nz
    Fth_drift = drift_coeff * (-np.sin(TH) * nx + np.cos(TH) * ny)

    return (Fz_base + Fz_drift, Fth_base + Fth_drift, p_base)


# ============================================================
# ПОТОКИ ИМПУЛЬСА СО СТОРОНЫ ХАОТИЧЕСКОЙ СРЕДЫ (СЭ)
# ============================================================
def chaos_forces(TH, PSI, chi, s_sh, kz, A):
    """
    Интеграл потока импульса от СЭ на ГП (макро-ярлык «давление СЭ»).
    СЭ действует снаружи, поэтому сила на ГП направлена вдоль −n̂.
    """
    nx, ny, nz = deformed_normal(TH, PSI, chi, s_sh, kz, A)
    Fz = -p_SE * nz
    Fth = -p_SE * (-np.sin(TH) * nx + np.cos(TH) * ny)
    return Fz, Fth


# ============================================================
# ВЫЧИСЛЕНИЕ ДРЕЙФА
# ============================================================
def compute_drift(chi, s_sh, v_n, v_ch, n_ch, A, m_S=1, q=1.0):
    """
    Вычисляет дрейфовую скорость V_∥ из интегралов потоков импульса.

    Параметры:
      chi – хиральность (±1)
      s_sh – направление бега шнека (-1,0,+1)
      q – параметр намотки двутория (геометрический фактор спиральной связи)
    Возвращает:
      V_par – продольная скорость дрейфа
      stats – словарь с промежуточными величинами
    """
    wp = wave_params(r_minor, m_S)
    kz = wp['k_z']
    th = np.linspace(0, 2 * np.pi, N_TH, endpoint=False)
    psi = np.linspace(0, 2 * np.pi, N_PSI, endpoint=False)
    TH, PSI = np.meshgrid(th, psi, indexing='ij')
    dth = 2 * np.pi / N_TH
    dpsi = 2 * np.pi / N_PSI

    # Интегралы потоков импульса
    Fz_ch, Fth_ch, p_ch = channel_forces(TH, PSI, chi, s_sh, v_n, v_ch, n_ch, kz, A)
    Fz_se, Fth_se = chaos_forces(TH, PSI, chi, s_sh, kz, A)

    # Усреднение по поверхности Σ (площадь 2π·r_minor·Lz, но Lz нормирована)
    def avg(F):
        return np.sum(F) * dth * dpsi / (2 * np.pi)

    Fth_ch_avg = avg(Fth_ch)
    Fth_se_avg = avg(Fth_se)
    Fth_avg = Fth_ch_avg + Fth_se_avg

    # Вращательный момент (Ω) из дисбаланса потока импульса
    # γ = n_a·m_a·v_0·2πr – нормировка (характеристика СЭ)
    gamma = n_a * m_a * v_0 * (2 * np.pi * r_minor)
    Omega = -Fth_avg / (gamma * r_minor) if gamma * r_minor != 0 else 0.0

    # Геометрический фактор преобразования вращения в дрейф (спиральная связь)
    coupling = float(s_sh) * q / np.sqrt(1 + q**2) if s_sh != 0 else 0.0
    V_par = coupling * Omega * r_minor

    stats = {
        'Fth_ch': Fth_ch_avg,
        'Fth_se': Fth_se_avg,
        'Fth_total': Fth_avg,
        'Omega': Omega,
        'coupling': coupling,
        'p_ch': float(p_ch),
        'p_SE': p_SE,
        'p_ratio': float(p_ch) / p_SE,
    }
    return V_par, stats


# ============================================================
# ОСНОВНОЙ БЛОК: ЗАПУСК И ПРОВЕРКИ
# ============================================================
def main():
    print("╔════════════════════════════════════════════════════════════════════╗")
    print("║  DRIFT v5: САМОСОГЛАСОВАННЫЙ КУБК (p_канал/p_СЭ → 1)              ║")
    print("║  Все величины «давление», «сила» – макро-ярлыки потоков импульса  ║")
    print("║  CANON §1, §2.3, §4.5–4.6, §8, §17                               ║")
    print("╚════════════════════════════════════════════════════════════════════╝")

    # Параметры канала из КУБК
    ch = close_params()
    v_n = ch['v_n']
    v_ch = ch['v_ch']
    n_ch = ch['n_ch']
    A = close_amplitude(r_minor)
    m_S = 1
    q = 1.0

    print(f"\n  |v_n| = {v_n:.2f} v₀, |v| = {v_ch:.2f} v₀")
    print(f"  n_ch = {n_ch / n_a:.3f}·n_a, A = {A:.2f} d_A")
    print(f"  Поток импульса СЭ (p_SE) = {p_SE:.4f}")

    # Проверка КУБК (баланс изотропных потоков импульса)
    p_ch_expected = n_ch * m_a * v_ch**2 / (4 * d_A**2)
    print(f"\n  КУБК-БАЛАНС ПОТОКОВ ИМПУЛЬСА:")
    print(f"    канал: p_канал = {p_ch_expected:.4f}")
    print(f"    СЭ:    p_СЭ    = {p_SE:.4f}")
    print(f"    p_канал/p_СЭ = {p_ch_expected/p_SE:.6f} → {'✅ = 1.00' if abs(p_ch_expected/p_SE - 1) < 1e-10 else '❌'}")

    # Таблица для всех комбинаций χ и s_sh
    print(f"\n{'─'*95}")
    print(f"{'χ':>3} {'s_sh':>3} {'Fθ(канал)':>13} {'Fθ(СЭ)':>13} {'Fθ(сумм)':>13} "
          f"{'Ω':>13} {'V_∥':>13} {'p_канал/p_СЭ':>12}")
    print(f"{'─'*95}")

    results = {}
    for chi in [+1, -1]:
        for s in [+1, 0, -1]:
            V, d = compute_drift(chi, s, v_n, v_ch, n_ch, A, m_S, q)
            results[(chi, s)] = (V, d)
            print(f"{chi:+3d} {s:+3d} {d['Fth_ch']:+13.4e} {d['Fth_se']:+13.4e} "
                  f"{d['Fth_total']:+13.4e} {d['Omega']:+13.4e} {V:+13.4e} "
                  f"{d['p_ratio']:12.6f}")

    # Проверки симметрий CANON §17
    H = "=" * 95
    print(f"\n{H}\nПРОВЕРКИ СИММЕТРИЙ (CANON §17)\n{H}")

    # Баланс потоков импульса
    print(f"\n  p_канал/p_СЭ = {results[(+1,+1)][1]['p_ratio']:.6f} → "
          f"{'✅ самосогласован' if abs(results[(+1,+1)][1]['p_ratio']-1) < 1e-3 else '❌'}")

    print("\n  χ-НЕЧЁТНОСТЬ V_∥:")
    for s in [+1, -1]:
        Vp = results[(+1, s)][0]
        Vm = results[(-1, s)][0]
        delta = abs(Vp + Vm) / max(abs(Vp), abs(Vm), 1e-30)
        print(f"    s_sh={s:+d}: V_∥(χ=+)={Vp:+.4e}, V_∥(χ=−)={Vm:+.4e}, δ={delta:.2e} → "
              f"{'✅' if delta < 1e-10 else '❌'}")

    print("\n  РЕВЕРС БЕГА ШНЕКА (s_sh → -s_sh):")
    for chi in [+1, -1]:
        Vf = results[(chi, +1)][0]
        Vr = results[(chi, -1)][0]
        delta = abs(Vf + Vr) / max(abs(Vf), abs(Vr), 1e-30)
        print(f"    χ={chi:+d}: V_∥(→)={Vf:+.4e}, V_∥(←)={Vr:+.4e}, δ={delta:.2e} → "
              f"{'✅' if delta < 1e-10 else '❌'}")

    print("\n  СТОЯЧИЙ ШНЕК (s_sh=0):")
    for chi in [+1, -1]:
        V0 = results[(chi, 0)][0]
        print(f"    χ={chi:+d}: V_∥={V0:.2e} → {'✅' if abs(V0) < 1e-15 else '❌'}")

    print("\n  ОТСУТСТВИЕ ДЕФОРМАЦИИ (A=0):")
    V_flat, _ = compute_drift(+1, +1, v_n, v_ch, n_ch, 0.0, m_S, q)
    print(f"    V_∥={V_flat:.2e} → {'✅' if abs(V_flat) < 1e-15 else '❌'}")

    print("\n  НАМОТКА q=0 (нет спиральной связи):")
    V_q0, _ = compute_drift(+1, +1, v_n, v_ch, n_ch, A, m_S, q=0)
    print(f"    V_∥={V_q0:.2e} → {'✅' if abs(V_q0) < 1e-15 else '❌'}")

    # КУБК-инвариант (V_∥ не зависит от |v_n|)
    print("\n  КУБК-ИНВАРИАНТ (V_∥ не зависит от |v_n|):")
    for vr in [2.0, 3.0, 5.0, 10.0]:
        vn2 = vr * v_0
        vc2 = vn2 / 2.0
        nc2 = v_0**2 / vc2**2
        V, d = compute_drift(+1, +1, vn2, vc2, nc2, A, m_S, q)
        print(f"    |v_n|={vr:.0f}v₀: n_ch={nc2/n_a:.3f}n_a, p_r={d['p_ratio']:.4f}, V_∥={V:+.4e}")

    # Степенной закон V_∥ ∝ A^α
    print(f"\n{H}\nСТЕПЕННОЙ ЗАКОН V_∥ ∝ A^α\n{H}")
    As = [0.5, 1.0, 2.0, A, A * 2]
    As.sort()
    Vs = []
    for Av in As:
        V, _ = compute_drift(+1, +1, v_n, v_ch, n_ch, Av, m_S, q)
        Vs.append(V)
        print(f"  A={Av:6.2f} d_A: V_∥={V:+.4e}")
    if Vs[0] != 0 and Vs[-1] != 0:
        alpha = np.log(abs(Vs[-1] / Vs[0])) / np.log(As[-1] / As[0])
        print(f"  → V_∥ ∝ A^{alpha:.2f}")

    V_ref = abs(results[(+1, +1)][0])
    print(f"\n{H}")
    print(f"  V_∥ = {V_ref:.6e} · v₀")
    print(f"  V_∥/v_s = {V_ref / (3 * np.sqrt(2) * v_0):.6e}")
    print(f"{H}")


if __name__ == '__main__':
    main()
