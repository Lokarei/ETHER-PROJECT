"""
Амплитуда A из самосогласованного баланса (drift_model_v7)
==========================================================
B (Математика), 2026-03-29 | A-DIR-AMP-001
 
СТАТИЧЕСКИЙ АНАЛИЗ (§2.6 директивы):
  ⟨p_канал(ψ)⟩ = p_СЭ · I(a),  I(a) = 1/(1−a²)^(3/2),  a = A/r
  I(a) > 1 при a > 0 → статический баланс даёт ТОЛЬКО A = 0.
  Волна шнека — ДИНАМИЧЕСКИЙ режим.
 
ЭНЕРГЕТИЧЕСКИЙ БАЛАНС:
  P_drive = мощность впрыска от канального потока в волну
  P_damp  = мощность диссипации (столкновительная)
  P_drive(A) = P_damp(A) → стационарная амплитуда A_eq
 
  P_drive: направленный поток v_n набегает на волну с фазовой скоростью v_ph.
    Разница скоростей (v_n − v_ph) создаёт нетто-работу над волной.
    P_drive ∝ n_ch · m_a · v_ch · (v_n − v_ph) · (A·k_z)² · S_GP
 
  P_damp: столкновения на ГП рассеивают волновую энергию в хаос.
    Каждое столкновение на деформированном участке имеет «нелинейный хвост»
    пропорциональный (A·k_z)² от наклона нормали.
    P_damp ∝ p_СЭ · v₀ · (A·k_z)² · S_GP  (по размерности)
 
  Баланс P_drive = P_damp:
    n_ch · v_ch · (v_n − v_ph) · (Ak)² = p_СЭ · v₀ · (Ak)²/m_a · C_damp
 
  (Ak)² сокращается → A НЕ определяется из этого баланса!
  Это означает: при P_drive > P_damp волна растёт без ограничения (линейная неустойчивость).
  Ограничивает рост НЕЛИНЕЙНОЕ НАСЫЩЕНИЕ.
 
НЕЛИНЕЙНОЕ НАСЫЩЕНИЕ:
  При конечном A/r диссипация растёт быстрее, чем драйв:
  P_damp(A) ∝ (Ak)² + β·(Ak)⁴ + ...  (нелинейные поправки от компрессии)
  P_drive(A) ∝ (Ak)² · [1 − α·(A/r)² + ...]  (насыщение при сжатии канала)
 
  Стационарная точка: P_drive(A) = P_damp(A) при конечном A/r.
 
РЕАЛИЗАЦИЯ: баланс мощностей с нелинейными поправками → A_eq.
"""
 
import numpy as np
from scipy.optimize import brentq
 
d_A = 1.0; v_0 = 1.0; m_a = 1.0
n_a = 16/(27*np.sqrt(2)*d_A**3)
p_SE = m_a*v_0**2/(4*d_A**2)
G_GAS = 1.0/3.0
nu_coll = 8*np.sqrt(2)/(27*np.pi*d_A)*v_0  # частота столкновений
 
R_major = 2500.0; r_minor = 50.0
Q_MIN = 3
 
 
def wave_params(r, m_S=1):
    k=1.0/(m_S*r); kz=k/np.sqrt(2)
    v_ph = v_0  # фазовая скорость волны ~ v₀
    return {'k_z':kz,'k_mag':k,'v_ph':v_ph,'Lz_min':np.sqrt(2)*np.pi*m_S*r}
 
 
def close_params(v_n_over_v0=5.0, v_ratio=2.0):
    v_n=v_n_over_v0*v_0; v_ch=v_n/v_ratio
    n_ch=v_0**2/(4*d_A**2*G_GAS*v_ch**2)
    return {'v_n':v_n,'v_ch':v_ch,'n_ch':n_ch,'n_ratio':n_ch/n_a}
 
 
# ============================================================
# 1) СТАТИЧЕСКИЙ АНАЛИЗ
# ============================================================
def I_static(a):
    """I(a) = ⟨1/(1−a·sinψ)²⟩_ψ = 1/(1−a²)^(3/2)."""
    if abs(a) >= 1: return np.inf
    return 1.0 / (1 - a**2)**1.5
 
 
# ============================================================
# 2) ЭНЕРГЕТИЧЕСКИЙ БАЛАНС
# ============================================================
def power_drive(A, r, v_n, v_ch, n_ch, m_S=1):
    """
    Мощность впрыска от канального потока в волну.
 
    Физика: направленный поток v_n вдоль спирали h_A набегает на
    волну шнека с фазовой скоростью v_ph. Разница (v_n − v_ph)
    создаёт нетто-работу. Передача — через χ-нечётный момент F_θ.
 
    P_drive = |F_θ(A)| · |Ω(A)| · (2πR·r)
            ∝ (n_ch·v_ch²·A³·k_z³) · (n_ch·v_ch²·A³·k_z³/(γ·r))
            ∝ A⁶ · (...)
 
    НО: это мощность в дрейф, не в волну. Мощность в волну:
    P_wave_drive = n_ch · m_a · v_ch · |v_n − v_ph| · (A·k_z)² · 2πr
                   × C_drive  (геометрический коэффициент)
 
    С учётом нелинейного насыщения (компрессия канала при A/r):
    P_drive(A) = P_drive_0 · (Ak)² · (1 − (A/r)²)  
                 ↑ компрессия уменьшает n_ch_eff при большом A
    """
    wp = wave_params(r, m_S)
    kz = wp['k_z']; v_ph = wp['v_ph']
    ak = A * kz
    a = A / r  # безразмерная амплитуда
 
    # Линейный драйв
    C_drive = 0.5  # геометрический коэффициент (порядок единицы)
    P_lin = G_GAS * n_ch * m_a * v_ch * abs(v_n - v_ph) * ak**2 * (2*np.pi*r) * C_drive
 
    # Нелинейное насыщение: при компрессии канала n_ch растёт, но
    # эффективная площадь контакта уменьшается пропорционально (1−a²)
    P_drive = P_lin * (1 - a**2) if a < 1 else 0.0
 
    return P_drive
 
 
def power_damp(A, r, m_S=1):
    """
    Мощность столкновительной диссипации.
 
    На деформированной ГП каждое столкновение переводит часть
    волновой энергии в хаотическое движение. Скорость диссипации:
 
    P_damp = p_СЭ · v₀ · (A·k_z)² · 2πr · C_damp
             + p_СЭ · v₀ · (A·k_z)⁴ · 2πr · C_nl  (нелинейная добавка)
 
    C_damp — коэффициент диссипации на одно столкновение.
    Оценка: C_damp ~ ν_coll · d_A / v₀ ~ 0.133 (порядок длины пробега / d_A).
 
    Нелинейная добавка ∝ (Ak)⁴ обеспечивает насыщение:
    при больших A диссипация растёт быстрее, чем драйв.
    """
    wp = wave_params(r, m_S)
    kz = wp['k_z']
    ak = A * kz
 
    C_damp = nu_coll * d_A / v_0  # ~ 0.133
    C_nl = 3.0  # нелинейный коэффициент (от I(a) ≈ 1 + 3a²/2 + ...)
 
    P_damp = p_SE * v_0 * (2*np.pi*r) * C_damp * (ak**2 + C_nl * ak**4)
 
    return P_damp
 
 
def find_equilibrium_A(r, v_n, v_ch, n_ch, m_S=1):
    """
    Найти A_eq из P_drive(A) = P_damp(A).
 
    Баланс: 0 при A=0 (тривиальное решение), ненулевое A_eq если
    линейный драйв > линейная диссипация (неустойчивость).
    """
 
    def balance(A):
        if A <= 0: return 0.0
        return power_drive(A, r, v_n, v_ch, n_ch, m_S) - power_damp(A, r, m_S)
 
    # Проверяем: есть ли неустойчивость (драйв > диссипация при малых A)
    A_test = 0.01 * d_A
    if balance(A_test) <= 0:
        return 0.0, False  # волна затухает
 
    # Ищем пересечение (драйв = диссипация)
    A_max = 0.99 * r  # физическое ограничение
    try:
        A_eq = brentq(balance, A_test, A_max, xtol=1e-6)
        return A_eq, True
    except ValueError:
        # Драйв всегда больше диссипации до A_max → берём A_max
        return A_max * 0.9, True
 
 
